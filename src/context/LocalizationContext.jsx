export const LocalizationProvider = ({ children }) => {
  const { user } = useAuth()
  const [currentLanguage, setCurrentLanguage] = useState('en')
  const [direction, setDirection] = useState('ltr')

  // Load language preference
  useEffect(() => {
    // Check for user preference first
    const userLanguage = localStorage.getItem(`user_language_${user?.id}`)
    if (userLanguage) {
      setCurrentLanguage(userLanguage)
    } else {
      // Fallback to global setting or browser language
      const globalLanguage = localStorage.getItem('global_app_language')
      if (globalLanguage) {
        setCurrentLanguage(globalLanguage)
      } else {
        // Fallback to browser language or default
        const browserLang = navigator.language.split('-')[0]
        const supportedLang = ['en', 'ar'].includes(browserLang) ? browserLang : 'en'
        setCurrentLanguage(supportedLang)
      }
    }
  }, [user?.id])

  // Update direction based on language
  useEffect(() => {
    const isRTL = currentLanguage === 'ar'
    setDirection(isRTL ? 'rtl' : 'ltr')
    
    // Update document direction and language
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr'
    document.documentElement.lang = currentLanguage
    
    // Update body class for RTL styling
    document.body.className = document.body.className
      .replace(/\b(rtl|ltr)\b/g, '')
      .trim() + ` ${isRTL ? 'rtl' : 'ltr'}`
  }, [currentLanguage])

  // Change language (users can set their own preference)
  const changeLanguage = (language) => {
    setCurrentLanguage(language)
    
    if (user) {
      // Save user preference
      localStorage.setItem(`user_language_${user.id}`, language)
      
      // If Super Admin, also update global default
      if (user.role === 'SUPER_ADMIN') {
        localStorage.setItem('global_app_language', language)
      }
    }
    
    // Could also sync to server for cross-device consistency
    // await syncLanguageToServer(language)
  }

  // Get translation with fallback
  const t = (key, params = {}) => {
    let translation = translations[currentLanguage]?.[key] || translations.en[key] || key
    
    // Replace parameters in translation
    Object.keys(params).forEach(param => {
      translation = translation.replace(`{${param}}`, params[param])
    })
    
    return translation
  }

  // Get translation for interpolated strings
  const tr = (key, params = {}) => t(key, params)

  // Check if current language is RTL
  const isRTL = () => currentLanguage === 'ar'

  // Get supported languages
  const getSupportedLanguages = () => [
    { code: 'en', name: 'English', nativeName: 'English' },
    { code: 'ar', name: 'Arabic', nativeName: '' }
  ]

  // Format number based on locale
  const formatNumber = (number, options = {}) => {
    const locale = currentLanguage === 'ar' ? 'ar-SA' : 'en-US'
    return new Intl.NumberFormat(locale, options).format(number)
  }

  // Format currency based on locale
  const formatCurrency = (amount, currency = 'OMR') => {
    const locale = currentLanguage === 'ar' ? 'ar-SA' : 'en-US'
    return new Intl.NumberFormat(locale, {
      style: 'currency',
      currency: currency,
      minimumFractionDigits: 3,
      maximumFractionDigits: 3
    }).format(amount)
  }

  const value = {
    currentLanguage,
    direction,
    isRTL: isRTL(),
    changeLanguage,
    getSupportedLanguages,
    t,
    tr,
    formatNumber,
    formatCurrency
  }

  return (
    <LocalizationContext.Provider value={value}>
      {children}
    </LocalizationContext.Provider>
  )
}

export default LocalizationProvider